//Write a series of Conditional tests. Print a statement describing each test and your prediction for the result of each test. 
var car = 'subaru';
//Test 1: Equality comparison (True)
console.log("Is car == 'subaru'? I predict True.");
console.log(car == 'subaru');
//Test 2: Strict Equality comparison (True)
console.log("Is car === 'subaru'? I predict True.");
console.log(car === 'subaru');
//Test 3: Inequality comparison (False)
console.log("Is car != 'subaru'? I predict False.");
console.log(car != 'subaru');
//Test 4: Strict Inequality comparison (False)
console.log("Is car !== 'subaru'? I predict False.");
console.log(car !== 'subaru');
//Test 5: Less than comparison (False)
console.log("Is car < 'subaru'? I predict False.");
console.log(car < 'subaru');
//Test 6: Greater than comparison (False)
console.log("Is car > 'subaru'? I predict False.");
console.log(car > 'subaru');
//Test 7: Less than or equal to comparison (True)
console.log("Is car <= 'subaru'? I predict True.");
console.log(car <= 'subaru');
//Test 8: Greater than or equal to comparison (true)
console.log("Is car >= 'subaru'? I predict True.");
console.log(car >= 'subaru');
//Test 9: Checking Truthiness (True)
console.log("Is  it a car ? I predict True.");
console.log(car);
//Test 10; Checking Falseness (false)
console.log("Is it not a car ? I predict False.");
console.log(!car);
